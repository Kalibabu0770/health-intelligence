-- 9. FUNCTIONS & TRIGGERS
-- ================================================================

-- Auto-update updated_at timestamp
create or replace function update_updated_at_column()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create trigger update_smart_belt_patients_updated_at
  before update on smart_belt_patients
  for each row execute function update_updated_at_column();

-- ================================================================
-- 10. DAILY DATA CLEANUP FUNCTION
-- ================================================================

-- Function to delete sensor data older than 24 hours
create or replace function cleanup_old_sensor_data()
returns void as $$
begin
  delete from smart_belt_sensor_data
  where timestamp < now() - interval '24 hours';
  
  raise notice 'Deleted sensor data older than 24 hours';
end;
$$ language plpgsql;

